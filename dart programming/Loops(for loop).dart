void main() {
  //Loops
  //FOR LOOP
  // for (initilize; condition; increment/decrement) {

  // }

  //i++ -> i=i+1
  //i-- -> i=i-1
  //i+=2 -> i = i+2

  for (int i = 0; i <= 10; i++) {
    print('Hello World $i');
  }

  String value = 'Hello';
  for (int i = 0; i < value.length; i++) {
    print(value[i]);
  }
}
